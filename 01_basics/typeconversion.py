# In python there are 2 type of type conversion

# 1.implict type conversion->built in feature for certain data types
pi=3.14
a=78
sum=pi+a
print(type(sum))
# here float+int=float

# 2.casting/explict conversion->manual way of converting type
ans_1=int(pi+a)
print(type(ans_1))
# forcefully convrted into int