# oop concept 
# object oriented programming
# class in oops
class student: #class
    year=4
    course="b.tech"

stu_1=student() #object

print(type(stu_1))

#operations on class similar to functions right
print(stu_1)
print(stu_1.year)
print(stu_1.course)

